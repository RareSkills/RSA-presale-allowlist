from loadKeyPair import load

def viewKeys():
    # load key values
    n, e, d = load()

    print('\n==========================================================================================')
    print('================================Values for RSA===================================')
    print(f'Small prime, e: {"0x" + str(hex(e))[2:].zfill(64)}\n') # pad to 32 bytes
    print(f'Modulus,     n: {hex(n)}\n')
    print('==========================================================================================\n')

    print('==========================================================================================')
    print('================================Private key (off-chain)===================================')
    print(f'private key: {hex(d)}\n')
    print('==========================================================================================')
